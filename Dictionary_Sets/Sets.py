# Sets In Python 

# A set is a collection which is unordered, unchangeable*, and unindexed.
# * Unchangeable, means that once a set is created, you cannot change its items, but you can remove items and add new items.
# In Python sets are written with curly brackets.

thisset = {"apple", "banana", "cherry"}
print(thisset)
