a="Hello World!"
print(a[0]) # indexing
print(a[-1]) # reverse indexing
print(a[0:5]) # slicing
print(a[0:5:2]) # slicing
print(a[0:]) # slicing
print(a[:5]) # slicing
print(a[:]) # slicing
print(a.format()) # formatting
print(a.upper())  # Upper case
print(a.lower())  # Lower case
print(a.title())  # Title case
print(a.capitalize()) # Capitalize
print(a.swapcase()) # Swap case
print(a.isupper()) # boolean
print(a.islower()) # boolean
print(a.istitle()) # boolean
