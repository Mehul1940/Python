# Escape Sequence in Python 
a="Hello \n World!" # new line
print(a)
b="Hello \t World!" # tab
print(b)
c="Hello \b World!" # backspace
print(c)
d="Hello \\ World!" # backslash
print(d)
e="Hello \r World!" # carraige return
print(e)
f="Hello \f World!" # form feed
print(f)
g="Hello \v World!" # vertical tab
print(g)
h="Hello \a World!" # alert
print(h)
i="Hello \x41 World!" # hex
print(i)
j="Hello \u0041 World!" # unicode
print(j)
k="Hello \U00000041 World!" # unicode
print(k)    