# String Function in Python 
a="Hello World!"
print(len(a)) # length
print(a.count("l")) # count
print(a.find("o")) # find
print(a.index("o")) # index
print(a.replace("Hello", "Goodbye")) # replace
print(a.split(" ")) # split
print(a.endswith("!")) # endswith
print(a.startswith("Hello")) # startswith
print(a.capitalize()) # capitalize
print(a.upper()) # upper
print(a.lower()) # lower
print(a.title()) # title
print(a.swapcase()) # swapcase
print(a.isupper()) # boolean
print(a.islower()) # boolean
print(a.istitle()) # boolean